import os
import yfinance as yf
import pandas as pd
import warnings

warnings.filterwarnings("ignore")

# Target asset configuration (e.g. "AAPL", "NVDA", "JPM")
ticker = "NVDA"
start_date = "2014-01-01"
end_date = "2026-05-31"

print(f"Calculating volume-weighted average price (Average) for {ticker}...")

# Fetch the raw historical dataset from Yahoo Finance
df = yf.download(ticker, start=start_date, end=end_date, auto_adjust=True)

# Flatten the multi-level column index if needed (fixes yfinance formatting changes)
if isinstance(df.columns, pd.MultiIndex):
    df.columns = df.columns.get_level_values(0)

# Calculating daily VWAP (Volume Weighted Average Price)
# Since we don't have tick-by-tick data, we approximate the daily volume-weighted average.
df['Typical_Price'] = (df['High'] + df['Low'] + df['Close']) / 3
df['Money_Flow'] = df['Typical_Price'] * df['Volume']

# True traded value of the asset
df['Average'] = df['Money_Flow'] / df['Volume']

# Compute daily asset returns
df['Daily_Return'] = df['Close'].pct_change()

# Drop any incomplete rows generated by the percentage change step
df.dropna(inplace=True)

# Locating the 'data' directory inside the project root
script_dir = os.path.dirname(os.path.abspath(__file__))
project_root = os.path.dirname(script_dir) if "scripts" in script_dir else script_dir
data_dir = os.path.join(project_root, 'data')

# Ensure the data directory exists before saving
os.makedirs(data_dir, exist_ok=True)

# Export the engineered features directly to the target Data folder
output_filename = f"{ticker}_data.csv"
output_path = os.path.join(data_dir, output_filename)
df.to_csv(output_path)

print(f"\n✨ Success! Dataset saved directly to: {output_path}")
print(df[['Open', 'Close', 'Volume', 'Average']].head())